// Tipos de datos
#include <iostream>

using namespace std;

int main()
{
    // TIPOS DE DATOS

    //entero
    cout << "Tipo de dato Entero" << endl;
    cout << 10 << endl;
    cout << endl;
    //flotante
    cout << "Tipo de dato Flotante" << endl;
    cout << 3.1416f << endl;
    cout << endl;
    //Boleano
    cout << "Tipo de dato Booleano" << endl;
    cout << true << endl;
    cout << false << endl;
    cout << endl;

    //char
    cout << "Tipo de dato Caracter" << endl;
    cout << 'C' << endl;
    cout << endl;

    //string
    cout << "Tipo de dato String" << endl;
    cout << "Hola Mundo!" << endl;
    cout << endl;
    return 0;
}
